export function loadSystemPrompt(stage: string): string {
  const stageKey = stage as Stage;
  const stageSteps = STAGE_STEPS[stageKey] ?? STAGE_STEPS['T'];
  return `${COMMON_GUIDELINES}\n\n${stageSteps}`;
}

export interface PageContext {
  projectTitle?: string;
  activePhase?: string;
  activeSection?: string;
  activityInputs?: Record<string, string>;
  selectedActivityCode?: string;
  referenceFiles?: { name: string; mime: string; content?: string; pdfData?: string }[];
  selectedStandards?: { code: string; subject: string; domain: string; content: string }[];
  selectedIdeas?: { id: string; subject: string; domain: string; content: string }[];
  opinions?: { activityCode: string; question: string; responses: { name: string; text: string }[] }[];
  teamMembers?: { name: string; subject: string }[]; // R6: 팀 프로필
}

// R5 요약: 선택 카드 제외 선행 카드를 카드당 200자로 잘라 반환
function buildR5Summary(
  activityInputs: Record<string, string>,
  selectedCode?: string,
): { code: string; summary: string }[] {
  return Object.entries(activityInputs)
    .filter(([code, v]) => v.trim() && code !== selectedCode)
    .map(([code, value]) => ({
      code,
      summary: value.length > 200 ? value.slice(0, 200) + '…' : value,
    }));
}

export function buildPageContextBlock(ctx: PageContext): string {
  const code = ctx.selectedActivityCode;
  const res = getCardResources(code);
  const lines: string[] = [];

  lines.push('---');
  lines.push('## 현재 워크스페이스 상태');

  if (ctx.projectTitle) lines.push(`- 프로젝트명: ${ctx.projectTitle}`);
  if (ctx.activePhase)  lines.push(`- 현재 단계: ${ctx.activePhase}`);

  // ── 선택된 카드 + 지침 ────────────────────────────────────────────
  if (code) {
    lines.push(`- 현재 카드: **[${code}]**`);

    const selectedText = ctx.activityInputs?.[code]?.trim();
    if (selectedText) {
      lines.push('');
      lines.push('### 현재 카드 입력 내용');
      lines.push(selectedText);
    }

    const directive = STEP_DIRECTIVES[code];
    if (directive) {
      lines.push('');
      lines.push('### 이 카드의 AI 행동 지침');
      lines.push(directive);
    }

    // 확인 멘트
    lines.push('');
    lines.push(`### 응답 마지막 확인 멘트 (아래 문장으로 끝낼 것)`);
    lines.push(`"${getClosingMessage(code)}"`);
  }

  // ── R5: 선행 카드 요약 (항상 포함, 200자 제한) ───────────────────
  const r5 = buildR5Summary(ctx.activityInputs ?? {}, code);
  if (r5.length > 0) {
    lines.push('');
    lines.push('### 선행 카드 요약 (R5)');
    for (const { code: c, summary } of r5) {
      lines.push(`**[${c}]** ${summary}`);
    }
  }

  // ── R6: 팀 프로필 (T·DI 단계만) ─────────────────────────────────
  if (res.r6 && ctx.teamMembers && ctx.teamMembers.length > 0) {
    lines.push('');
    lines.push('### 팀 구성원 (R6)');
    for (const m of ctx.teamMembers) {
      lines.push(`- ${m.name}${m.subject ? ` (${m.subject})` : ''}`);
    }
  }

  // ── 팀원 의견 (항상 포함) ─────────────────────────────────────────
  if (ctx.opinions && ctx.opinions.length > 0) {
    lines.push('');
    lines.push('### 팀원 의견');
    for (const op of ctx.opinions) {
      lines.push(`**[${op.activityCode}] "${op.question}"**`);
      for (const r of op.responses) {
        lines.push(`- ${r.name}: ${r.text}`);
      }
    }
  }

  // ── R1: 성취기준 (A-1-2, A-2-1만) ──────────────────────────────
  if (res.r1 && ctx.selectedStandards && ctx.selectedStandards.length > 0) {
    lines.push('');
    lines.push('### 성취기준 (R1)');
    lines.push('아래 성취기준을 핵심 분석 대상으로 삼으세요.');
    for (const s of ctx.selectedStandards) {
      const dp = s.domain ? ` · ${s.domain}` : '';
      lines.push(`**${s.code}** (${s.subject}${dp})`);
      lines.push(s.content);
    }
  }

  // ── R2: 핵심 아이디어 (A-1-2, A-2-1, A-2-2만) ───────────────────
  if (res.r2 && ctx.selectedIdeas && ctx.selectedIdeas.length > 0) {
    lines.push('');
    lines.push('### 핵심 아이디어 (R2)');
    lines.push('아래 핵심 아이디어를 수업 목표·활동 설계에 반영하세요.');
    for (const idea of ctx.selectedIdeas) {
      const dp = idea.domain ? ` · ${idea.domain}` : '';
      lines.push(`**${idea.subject}${dp}**`);
      lines.push(idea.content);
    }
  }

  // ── R3: 참고자료 (A-1-2 전문, Ds-1-2 전문, 나머지 200자 요약) ───
  if (ctx.referenceFiles && ctx.referenceFiles.length > 0) {
    const cap = res.r3 === true ? 3000 : res.r3 === 'summary' ? 200 : 0;
    if (cap > 0) {
      lines.push('');
      lines.push('### 참고자료 (R3)');
      for (const f of ctx.referenceFiles) {
        if (!f.content) { lines.push(`**[${f.name}]** — 텍스트 추출 불가`); continue; }
        const body = f.content.length > cap
          ? f.content.slice(0, cap) + `\n…(${f.content.length - cap}자 생략)`
          : f.content;
        lines.push(`**[${f.name}]**`);
        lines.push(body);
      }
    }
  }

  lines.push('---');
  if (code) {
    lines.push(`[${code}] 카드를 기준으로 위 지침에 따라 답변하세요.`);
  } else {
    lines.push('팀의 현재 진행 상황을 파악하고 구체적인 제안을 제공하세요.');
  }

  return lines.join('\n');
}
