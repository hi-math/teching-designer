export function loadSystemPrompt(stage: string): string {
  const stageKey = stage as Stage;
  const stageSteps = STAGE_STEPS[stageKey] ?? STAGE_STEPS['T'];
  return `${COMMON_GUIDELINES}\n\n${stageSteps}`;
}

interface PageContext {
  projectTitle?: string;
  activePhase?: string;
  activeSection?: string;
  activityInputs?: Record<string, string>;
  selectedActivityCode?: string;
  referenceFiles?: { name: string; mime: string; content?: string; pdfData?: string }[];
  selectedStandards?: { code: string; subject: string; domain: string; content: string }[];
  selectedIdeas?: { id: string; subject: string; domain: string; content: string }[];
  opinions?: { activityCode: string; question: string; responses: { name: string; text: string }[] }[];
}

export function buildPageContextBlock(ctx: PageContext): string {
  const lines: string[] = [];

  lines.push('---');
  lines.push('## 현재 워크스페이스 상태 (참고용)');

  if (ctx.projectTitle) {
    lines.push(`- 프로젝트명: ${ctx.projectTitle}`);
  }
  if (ctx.activePhase) {
    lines.push(`- 현재 단계: ${ctx.activePhase}`);
  }
  if (ctx.activeSection) {
    lines.push(`- 현재 섹션: ${ctx.activeSection}`);
  }

  // 선택된 활동 카드 + 해당 카드의 세부 프롬프팅 지침 주입
  if (ctx.selectedActivityCode) {
    lines.push(`- 현재 선택된 활동 카드: **[${ctx.selectedActivityCode}]**`);
    const selectedText = ctx.activityInputs?.[ctx.selectedActivityCode]?.trim();
    if (selectedText) {
      lines.push('');
      lines.push('### 선택된 활동 카드 내용 (주요 참고)');
      lines.push(`**[${ctx.selectedActivityCode}]**`);
      lines.push(selectedText);
    }

    // 활동 코드별 세부 지침 주입
    const directive = STEP_DIRECTIVES[ctx.selectedActivityCode];
    if (directive) {
      lines.push('');
      lines.push('### 이 활동에 대한 AI 행동 지침');
      lines.push(directive);
    }
  }

  const filled = Object.entries(ctx.activityInputs ?? {})
    .filter(([code, v]) => v.trim() && code !== ctx.selectedActivityCode);
  if (filled.length > 0) {
    lines.push('');
    lines.push('### 팀이 작성한 다른 활동 내용');
    for (const [code, value] of filled) {
      lines.push(`**[${code}]**`);
      lines.push(value.trim());
    }
  }

  if (ctx.opinions && ctx.opinions.length > 0) {
    lines.push('');
    lines.push('### 팀원 의견 (의견묻기 결과)');
    lines.push('팀원들이 아래 활동 카드에 대해 의견을 제출했습니다. 이 의견들을 충분히 반영하여 팀의 현재 관심사·방향성·고민을 이해하고, 그에 맞춘 구체적인 조언과 제안을 제공하세요.');
    for (const op of ctx.opinions) {
      lines.push('');
      lines.push(`**[${op.activityCode}] 질문: "${op.question}"**`);
      for (const r of op.responses) {
        lines.push(`- ${r.name}: ${r.text}`);
      }
    }
  }

  if (ctx.selectedStandards && ctx.selectedStandards.length > 0) {
    lines.push('');
    lines.push('### 수업 성취기준 (핵심 목표)');
    lines.push('아래 성취기준을 이 수업설계의 핵심 목표로 삼아, 모든 단계(목표 진술·평가 계획·학습활동 설계 등)에 일관되게 반영하세요.');
    for (const s of ctx.selectedStandards) {
      const domainPart = s.domain ? ` · ${s.domain}` : '';
      lines.push(`**${s.code}** (${s.subject}${domainPart})`);
      lines.push(s.content);
    }
  }

  if (ctx.selectedIdeas && ctx.selectedIdeas.length > 0) {
    lines.push('');
    lines.push('### 수업 핵심아이디어 (개념적 토대)');
    lines.push('아래 핵심아이디어는 이 수업이 지향하는 개념적·원리적 토대입니다. 학습목표 진술, 문제 상황 설계, 학습활동 구성 시 이 핵심아이디어가 학습자에게 내면화될 수 있도록 반영하세요.');
    for (const idea of ctx.selectedIdeas) {
      const domainPart = idea.domain ? ` · ${idea.domain}` : '';
      lines.push(`**${idea.subject}${domainPart}**`);
      lines.push(idea.content);
    }
  }

  if (ctx.referenceFiles && ctx.referenceFiles.length > 0) {
    lines.push('');
    lines.push('### 업로드된 참고자료');
    for (const f of ctx.referenceFiles) {
      if (f.content) {
        lines.push(`**[${f.name}]**`);
        lines.push(f.content.length > 3000 ? f.content.slice(0, 3000) + '\n…(이하 생략)' : f.content);
      } else {
        lines.push(`**[${f.name}]** (${f.mime || '파일'}) — 텍스트 추출 불가`);
      }
    }
    lines.push('');
    lines.push('위 참고자료를 바탕으로 팀의 질문에 구체적으로 답변하세요.');
  }

  lines.push('---');
  if (ctx.selectedActivityCode) {
    lines.push(`사용자가 [${ctx.selectedActivityCode}] 카드를 선택하여 대화를 시작했습니다. 위 활동 지침에 따라 해당 마이크로 스텝을 안내하고 도움을 제공하세요.`);
  } else {
    lines.push('위 내용을 바탕으로 팀의 현재 진행 상황을 파악하고, 질문에 답변하거나 구체적인 제안을 제공하세요.');
  }

  return lines.join('\n');
}
