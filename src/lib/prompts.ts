const closingVariantIdx: Record<string, number> = {};

function getClosingMessage(code: string): string {
  const variants = CLOSING_MESSAGES[code];
  if (!variants) return DEFAULT_CLOSING;
  const last = closingVariantIdx[code] ?? -1;
  const next = last === 0 ? 1 : 0;
  closingVariantIdx[code] = next;
  return variants[next];
}

